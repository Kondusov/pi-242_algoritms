// Задача
// Создайте два множества (Set): первое содержит числа 1, 2, 3, 4, второе — 3, 4, 5, 6.
// Реализуйте три операции:
// Объединение (Union) — элементы, которые есть хотя бы в одном из множеств.
// Пересечение (Intersection) — элементы, которые есть в обоих множествах одновременно.
// Разность (Difference) — элементы, которые есть в первом множестве, но отсутствуют во втором.

// 1. Создание множеств
const setA = new Set([1, 2, 3, 4]);
const setB = new Set([3, 4, 5, 6]);

console.log('Множество A:', [...setA]);
console.log('Множество B:', [...setB]);

// 2. Объединение (Union)
// Создаем новый массив из всех элементов обоих множеств, Set автоматически удалит дубликаты
const union = new Set([...setA, ...setB]);
console.log('Объединение:', [...union]); // [1, 2, 3, 4, 5, 6]

// 3. Пересечение (Intersection)
// Оставляем только те элементы из A, которые присутствуют в B
const intersection = new Set([...setA].filter(item => setB.has(item)));
console.log('Пересечение:', [...intersection]); // [3, 4]

// 4. Разность (Difference)
// Оставляем только те элементы из A, которых нет в B
const differenceA = new Set([...setA].filter(item => !setB.has(item)));
console.log('Разность (A - B):', [...differenceA]); // [1, 2]

// 4. Разность (B из A)
// Оставляем только те элементы из A, которых нет в B
const differenceB = new Set([...setB].filter(item => !setA.has(item)));
console.log('Разность (B - A):', [...differenceB]); // [1, 2]


// Как это работает:
// new Set(): создает коллекцию уникальных значений.
// Спред-оператор [...]: превращает множество в массив,
// чтобы мы могли использовать методы массивов (например, .filter()).set.has(value): очень быстрая операция (сложность \(O(1)\)),
// которая проверяет наличие элемента в множестве.